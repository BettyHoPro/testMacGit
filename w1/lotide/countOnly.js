countOnly = (allItems, itemsToCount) => {
  const results = {};
  for (const item of allItems) {
    console.log(item);
    //console.log(item + " : "+ itemsToCount[item] );
    if (itemsToCount[item]) {
      if (results[item]) {
        results[item] += 1;
      } else {
        results[item] = 1;
      }
    }
  }
  return results;
};

